// Инициализация автодополнения
function initCityAutocomplete() {
  var input = document.getElementById('input-city');
  var list  = document.getElementById('cities-list');
  if (!input || !list) return;

  input.addEventListener('input', function() {
    var val = this.value.trim().toLowerCase();
    list.innerHTML = '';

    if (val.length < 2) {
      list.style.display = 'none';
      return;
    }

    var matches = CITIES.filter(function(city) {
      return city.toLowerCase().startsWith(val);
    }).slice(0, 6);

    if (matches.length === 0) {
      list.style.display = 'none';
      return;
    }

    matches.forEach(function(city) {
      var item = document.createElement('div');
      item.className = 'city-item';
      item.textContent = city;
      item.addEventListener('click', function() {
        input.value = city;
        list.style.display = 'none';
      });
      list.appendChild(item);
    });

    list.style.display = 'block';
  });

  // Скрыть список при клике вне
  document.addEventListener('click', function(e) {
    if (e.target !== input) {
      list.style.display = 'none';
    }
  });
}

// Запускаем после загрузки DOM
document.addEventListener('DOMContentLoaded', initCityAutocomplete);
